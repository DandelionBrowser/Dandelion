"""Configures and builds Dandelion.

GN arguments come from //dandelion/build/args/<config>.gn, with optional
developer-local overrides appended from build/args/local.gn, which is not
tracked by git. The generated out/<config>/args.gn is therefore reproducible
from the repository alone.

Usage:
  python build/build.py                    # dev config, chrome target
  python build/build.py --config release
  python build/build.py --target unit_tests
  python build/build.py --gen-only
"""

import argparse
import os
import shutil
import sys

sys.path.insert(0, os.path.dirname(os.path.abspath(__file__)))

from lib import config
from lib import proc

LOCAL_ARGS_FILE = os.path.join(config.ARGS_DIR, 'local.gn')


def _compose_args(name):
  """Returns the contents of args.gn for a named configuration."""
  source = os.path.join(config.ARGS_DIR, '%s.gn' % name)
  if not os.path.exists(source):
    available = sorted(
        f[:-3] for f in os.listdir(config.ARGS_DIR) if f.endswith('.gn'))
    raise ValueError('unknown config %r; available: %s'
                     % (name, ', '.join(available)))

  with open(source, encoding='utf-8') as f:
    contents = f.read()

  parts = ['# Generated by //dandelion/build/build.py. Edit '
           'build/args/%s.gn instead.\n' % name, contents]
  if os.path.exists(LOCAL_ARGS_FILE):
    with open(LOCAL_ARGS_FILE, encoding='utf-8') as f:
      parts.append('\n# --- build/args/local.gn ---\n')
      parts.append(f.read())
  return ''.join(parts)


def _write_args(out, contents):
  os.makedirs(out, exist_ok=True)
  path = os.path.join(out, 'args.gn')
  with open(path, 'w', encoding='utf-8', newline='\n') as f:
    f.write(contents)
  return path


def main():
  parser = argparse.ArgumentParser(description=__doc__)
  parser.add_argument('--config', default='dev',
                      help='configuration in build/args (default: dev)')
  parser.add_argument('--target', default='chrome',
                      help='ninja target to build (default: chrome)')
  parser.add_argument('--gen-only', action='store_true',
                      help='run gn gen and stop, without building')
  args = parser.parse_args()

  src = config.chromium_src()
  if not os.path.isdir(os.path.join(src, '.git')):
    raise proc.CommandError(
        'no Chromium checkout at %s; run build/sync.py first' % src)
  if shutil.which('gn') is None or shutil.which('autoninja') is None:
    raise proc.CommandError(
        'gn and autoninja were not found on PATH. Add depot_tools to PATH.')

  out = config.out_dir(args.config)
  written = _write_args(out, _compose_args(args.config))
  proc.info('wrote %s' % written)

  # gn wants the out directory relative to the source root.
  proc.run(['gn', 'gen', os.path.relpath(out, src)], cwd=src)
  if args.gen_only:
    return 0

  proc.run(['autoninja', '-C', os.path.relpath(out, src), args.target],
           cwd=src)
  proc.info('built %s in %s' % (args.target, out))
  return 0


if __name__ == '__main__':
  try:
    sys.exit(main())
  except (proc.CommandError, ValueError) as e:
    proc.error(str(e))
    sys.exit(1)
