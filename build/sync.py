"""Creates or updates the Chromium checkout that Dandelion builds against.

The checkout is pinned to the version in //dandelion/CHROMIUM_VERSION and lives
outside this repository. This repository is then mounted into it as
src/dandelion, so that Chromium sources and Dandelion sources sit in one tree
while remaining two independent git repositories.

Usage:
  python build/sync.py               # sync to the pinned version and patch
  python build/sync.py --no-patch    # sync only, leave upstream pristine
"""

import argparse
import os
import shutil
import sys

sys.path.insert(0, os.path.dirname(os.path.abspath(__file__)))

from lib import config
from lib import patching
from lib import proc

_GCLIENT_TEMPLATE = """\
# Generated by //dandelion/build/sync.py. Do not edit by hand: edit
# CHROMIUM_VERSION and re-run the script instead.
solutions = [
  {{
    "name": "src",
    "url": "{url}",
    "managed": False,
    "custom_deps": {{}},
    # Profile-guided optimisation data, required by build/args/release.gn.
    "custom_vars": {{"checkout_pgo_profiles": True}},
  }},
]
target_os = {target_os}
"""


def _require_depot_tools():
  if shutil.which('gclient') is None:
    raise proc.CommandError(
        'gclient was not found on PATH. Add your depot_tools checkout to PATH '
        'before running this script.')


def _target_os():
  if sys.platform == 'win32':
    return ['win']
  if sys.platform == 'darwin':
    return ['mac']
  return ['linux']


def _write_gclient(root, version):
  path = os.path.join(root, '.gclient')
  contents = _GCLIENT_TEMPLATE.format(
      url='%s@%s' % (config.CHROMIUM_GIT_URL, version),
      target_os=repr(_target_os()))
  with open(path, 'w', encoding='utf-8', newline='\n') as f:
    f.write(contents)
  proc.info('wrote %s' % path)


def _bootstrap_checkout(root):
  """Performs the initial ~100 GB fetch when no checkout exists yet."""
  proc.info('no Chromium checkout found; fetching (this takes hours)')
  os.makedirs(root, exist_ok=True)

  # `fetch` refuses to run in a directory that already has a .gclient file.
  # One can be left behind by an earlier run that was interrupted before the
  # clone created src/.git, which is easy to do during a multi-hour download.
  # It is regenerated immediately after this, so discarding it is safe.
  stale = os.path.join(root, '.gclient')
  if os.path.exists(stale):
    proc.warn('discarding incomplete checkout config %s' % stale)
    os.remove(stale)

  proc.run(['fetch', '--nohooks', 'chromium'], cwd=root)


def _checkout_version(src, version):
  """Pins the Chromium tree to the tagged release."""
  proc.info('pinning Chromium to %s' % version)
  proc.git(['fetch', '--tags', 'origin'], cwd=src, capture=False)
  # A named branch at the tag keeps `git status` readable and gives patches a
  # stable base to diff against, unlike a detached HEAD.
  branch = 'dandelion-%s' % version
  existing = proc.run(['git', 'rev-parse', '--verify', branch],
                      cwd=src, check=False, capture=True)
  if existing.returncode == 0:
    proc.git(['checkout', branch], cwd=src, capture=False)
  else:
    proc.git(['checkout', '-b', branch, 'refs/tags/%s' % version],
             cwd=src, capture=False)


def _mount_overlay(src):
  """Links this repository into the Chromium tree as src/dandelion.

  A junction is used on Windows because, unlike a symlink, it needs neither
  administrator rights nor Developer Mode.
  """
  link = config.overlay_path()
  if os.path.exists(link):
    resolved = os.path.realpath(link)
    if resolved == os.path.realpath(config.DANDELION_ROOT):
      proc.info('overlay already mounted at %s' % link)
      return
    raise proc.CommandError(
        '%s already exists and points at %s, not this repository. Remove it '
        'and re-run.' % (link, resolved))

  if sys.platform == 'win32':
    proc.run(['cmd', '/c', 'mklink', '/J', link, config.DANDELION_ROOT])
  else:
    os.symlink(config.DANDELION_ROOT, link, target_is_directory=True)
  proc.info('mounted overlay at %s' % link)


def main():
  parser = argparse.ArgumentParser(description=__doc__)
  parser.add_argument('--no-patch', action='store_true',
                      help='do not apply Dandelion patches after syncing')
  parser.add_argument('--no-hooks', action='store_true',
                      help='skip gclient runhooks (skips toolchain setup)')
  args = parser.parse_args()

  _require_depot_tools()

  root = config.chromium_root()
  src = config.chromium_src()
  version = config.chromium_version()

  if not os.path.isdir(os.path.join(src, '.git')):
    _bootstrap_checkout(root)

  _write_gclient(root, version)
  _checkout_version(src, version)

  sync_command = ['gclient', 'sync', '--with_branch_heads', '--with_tags',
                  '--delete_unversioned_trees']
  if args.no_hooks:
    sync_command.append('--nohooks')
  proc.run(sync_command, cwd=root)

  _mount_overlay(src)

  if not args.no_patch:
    proc.info('applying Dandelion patches')
    for patch_file in patching.list_patches():
      state = patching.apply_patch(src, patch_file)
      proc.info('  %s %s' % (state, os.path.basename(patch_file)))

  proc.info('checkout ready at %s' % src)
  return 0


if __name__ == '__main__':
  try:
    sys.exit(main())
  except (proc.CommandError, ValueError) as e:
    proc.error(str(e))
    sys.exit(1)
